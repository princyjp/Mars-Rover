package InputLayer;
/*think about plateau being different shapes
 * creating plateaushape class  */
public class PlateauSize {
    private int length;
    private int breadth;

    public PlateauSize(int length, int breadth) {
        this.length = length;
        this.breadth = breadth;
    }

    public int getLength() {
        return length;
    }

    public int getBreadth() {
        return breadth;
    }

}
