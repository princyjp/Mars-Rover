package InputLayer;
/*think about plateau being different shapes
 * creating plateaushape class  */
public class PlateauSize {
    private int maxX;
    private int maxY;

    public PlateauSize(int maxX, int maxY) {
        this.maxX = maxX;
        this.maxY = maxY;
    }

    public int getMaxX() {
        return maxX;
    }

    public int getMaxY() {
        return maxY;
    }

}
